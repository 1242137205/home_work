#将浮点类型的数据或者整数类型的数据转换为str类型的数据
print(type(str(12.12)))
print(str(54.45))
print("@@@_" * 20)
#将字典类型数据转换为string类型数据
dict1 = {"小米":1000, "华为":999, "oppo":1200, "vivo":2000}
print(type(str(dict1)))
print(str(dict1))
print("@@@_"*20)
#将列表转换为string类型的数据
list1 = [1, 2, 3, 4, 5, 6, 6, 7]
print(type(str(list1)))
print(str(list1))
